#include "libft.h"

void	ft_putlld(long long n)
{
	if (n == -9223372036854775807LL - 1)
		write(1, "-9223372036854775808", 20);
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		ft_putlld(n / 10);
	ft_putchar((n % 10) + '0');
}
